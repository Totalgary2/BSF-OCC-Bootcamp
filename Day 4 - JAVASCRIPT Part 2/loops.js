//Loops


//For Loop
for (let i = 1; i <= 5; i++ ) {
    console.log('For loop', i);
}


//While Loop
let count = 1
while (count <= 3) {
    console.log('While loop', count);
    count++;
    console.log('While loop', count);
}


//do-while loop
let counter = 10;
do {
    console.log('Do while count:', counter)
    counter++;
}while (counter <= 5);